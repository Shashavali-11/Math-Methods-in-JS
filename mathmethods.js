// abs(x)
console.log(Math.abs(-5)) // 5
// ------------------------------------------------------------------>


// ceil(x)
console.log(Math.ceil(0.1))  // 1
// ------------------------------------------------------------------>

// floor(x)
console.log(Math.floor(0.8))  // 0
// ------------------------------------------------------------------>

// max(x1, x2, x3, ...)
console.log(Math.max(1,2,3,4)) // 4
// ------------------------------------------------------------------>

// random(x)
var a = Math.random() * 10000
var b = Math.ceil(a)
console.log(b) //1234



